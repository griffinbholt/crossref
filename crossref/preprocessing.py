# Tokenizer – If it splits text into words or subwords.
# Text Cleaner – If it removes unwanted characters, punctuation, or extra spaces.
# Normalizer – If it standardizes text (e.g., lowercasing, removing diacritics).
# Preprocessor – A general term for handling multiple text-cleaning tasks.
# Stopword Remover – If it removes common stopwords.
# Lemmatizer or Stemmer – If it reduces words to their base or root form.